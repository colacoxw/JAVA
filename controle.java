public class controle {
    public static void main(String[] args) {

        for (int i = 1; i <= 5; i++) {
            System.out.println("Valor de i: " + i + ".");
        }

        int j = 1;
        while (j <= 5) { 
            System.out.println("Valor de j: " + j + ".");
            j++;
        }

        int k = 1;
        do {
            System.out.println("Valor de k: " + k + ".");
            k++;
        } while (k <= 5);
    }

}
